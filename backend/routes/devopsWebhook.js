// 🔥 THE WEBHOOK ENDPOINT
router.post('/github-webhook', async (req, res) => {
    // Acknowledge immediately (GitHub expects fast response)
    res.status(200).json({ received: true, status: 'Zylron DevOps processing...' });

    const event = req.headers['x-github-event'];
    console.log(`🤖 Zylron DevOps: Received GitHub event: ${event}`);
    if (event !== 'push') return;

    const { repository, commits, pusher } = req.body;
    if (!commits || commits.length === 0) return;

    console.log(`🤖 Zylron DevOps: New push to ${repository?.full_name} by ${pusher?.name}`);

    // Review each commit
    for (const commit of commits.slice(0, 3)) { // Max 3 commits per push
        const review = await reviewCommit({
            id: commit.id,
            message: commit.message,
            modified: commit.modified,
            added: commit.added,
            author: commit.author
        });

        console.log(`🔍 Commit Review (${commit.id.substring(0,7)}):`, review.substring(0, 200));

        if (review.includes('ISSUE_FOUND')) {
            console.log('🚨 Issue found! Attempting auto-fix PR...');
            const branchName = `zylron-fix-${Date.now()}`;
            const prResult = await raisePR(
                repository.full_name,
                branchName,
                commit.modified?.[0] || 'README.md',
                `// Zylron DevOps: Auto-fix applied\n// Issue: ${review}`,
                review
            );
            console.log('🤖 PR Result:', prResult);
        }
    }
});

